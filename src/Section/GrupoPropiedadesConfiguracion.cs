        ///         </ul>
        ///     </para>
        /// </remarks>
        /// <param name="index">Índice o posición del elemento en la colección.</param>
        /// <returns></returns>
        public ColeccionPropiedadesConfiguracion this[int index]
        {
            get { return (ColeccionPropiedadesConfiguracion)BaseGet(index); }
            set
            {
                if (BaseGet(index) != null)
                {
                    BaseRemoveAt(index);
                }
                BaseAdd(index, value);
            }
        }

