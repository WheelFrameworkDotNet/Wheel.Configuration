        /// <returns></returns>
        protected override ConfigurationElement CreateNewElement()
        {
            return new ColeccionPropiedadesConfiguracion();
        }

        /// <summary>
        /// Obtiene la clave de un elemento de configuración.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
